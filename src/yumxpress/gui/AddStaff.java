    private boolean validateInputs() {
        if(txtEmailId.getText().trim().isEmpty()||txtName.getText().trim().isEmpty()||String.valueOf(pwd.getPassword()).trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill all the details");
            return false;
        }
        if(!Validator.isValidEmail(txtEmailId.getText().trim())){
            JOptionPane.showMessageDialog(null,"Wrong Email Id");
            return false;
        }
        return true;
           
    }

