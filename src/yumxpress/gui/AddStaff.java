    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(! validateInputs()){
            return;
        }
        try{
            StaffPojo staff=new StaffPojo();
            staff.setCompanyId(OwnerProfile.getCompanyId());
            staff.setEmail(txtEmailId.getText().trim());
            String strpwd=String.valueOf(pwd.getPassword());
            
            strpwd=PasswordEncryption.getEncryptedPassword(strpwd);
             
            staff.setPwd(strpwd);
            staff.setStaffName(txtName.getText().trim());
            String result=StaffDao.addStaff(staff);
            if(result==null){
                JOptionPane.showMessageDialog(null,"Can't add the staff");
                return;
            }
            JOptionPane.showMessageDialog(null,"staff added successfully");
            staff.setStaffId(result);
            Map<String,String>map=CompanyDao.getEmailCredintialByCompanyId(OwnerProfile.getCompanyId());
            Mailer.sendMail(map, staff);
            JOptionPane.showMessageDialog(null,"Mail Sent to "+txtEmailId.getText().trim());
            clearAll();
            return;
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB problem in adding the staff"+ex);
            return;
        }
        catch(MessagingException ex){
            JOptionPane.showMessageDialog(null,"Please Check your data connection ");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

