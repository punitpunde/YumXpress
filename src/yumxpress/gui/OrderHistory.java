    private void initTable() {
        try{
            list=OrderDao.getOrderHistoryDetail(UserProfile.getId());
            dfm=(DefaultTableModel)jtHistory.getModel();
            for(OrderPojo pojo:list){
                Object []obj=new Object[5];
                obj[0]=pojo.getOrderId();
                obj[1]=pojo.getProductName();
                obj[2]=pojo.getProductPrice();
                obj[3]=pojo.getCompanyName();
                obj[4]=pojo.getReview();
                dfm.addRow(obj);
            }
            
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
            ex.printStackTrace();
        }
        catch(IOException ex){
            JOptionPane.showMessageDialog(null, ex);
            
        }
    }
