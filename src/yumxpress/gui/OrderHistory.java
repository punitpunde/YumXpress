    private void jtHistoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtHistoryMouseClicked
        
        String review=JOptionPane.showInputDialog(null,"Enter review !");
        OrderPojo pojo=list.get(jtHistory.getSelectedRow());
        String id=pojo.getOrderId();
        Object[]obj=new Object[5];
        obj[0]=pojo.getOrderId();
        obj[1]=pojo.getProductName();
        obj[2]=pojo.getProductPrice();
        obj[3]=pojo.getCompanyName();
        pojo.setReview(review);
        obj[4]=pojo.getReview();       
        dfm.addRow(obj);
        try{
            boolean result=OrderDao.addReview(review,id);
            if(result){
                JOptionPane.showMessageDialog(null,"Thank You for giving review !!");
                dfm.removeRow(jtHistory.getSelectedRow());
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jtHistoryMouseClicked

