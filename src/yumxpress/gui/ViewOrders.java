    private void jTOrdersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTOrdersMouseClicked
        try{
            String strOtp=JOptionPane.showInputDialog(null,"Please Enter OTP");
            int otp=Integer.parseInt(strOtp);
            OrderPojo pojo=list.get(jTOrders.getSelectedRow());
           
            if(pojo.getOtp()==otp){
                JOptionPane.showMessageDialog(null,"You have given correct otp");
                boolean result=OrderDao.updateOrderStatus(pojo.getOrderId());
                if(result)
                {
                    JOptionPane.showMessageDialog(null,"Order Delivered succssecfully");
                    dfm.removeRow(jTOrders.getSelectedRow());
                   
                }
                else
                    JOptionPane.showMessageDialog(null,"Order not Delivered succssecfully");
            }
            else{
                JOptionPane.showMessageDialog(null,"You have given wrong otp");
            }
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"You have given wrong input");
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Exception while updating the record"+ex);
        }
    }//GEN-LAST:event_jTOrdersMouseClicked

