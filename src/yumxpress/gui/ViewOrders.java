private void initTable() {
    try{
        list=OrderDao.getAllOrderedPojoByStaffId("STF-101");
        if(list.isEmpty()){
//            JOptionPane.showMessageDialog(null,"No Undeliverd Food Item");
            return;
        }
         dfm=(DefaultTableModel)jTOrders.getModel();
        
        for(OrderPojo pojo:list){
            Object obj[]=new Object[5];
            obj[0]=pojo.getProductName();
            obj[1]=pojo.getProductPrice();
            obj[2]=pojo.getCustomerName();
            obj[3]=pojo.getCustomerPhoneNo();
            
            obj[4]=pojo.getCustomerAddress();
            dfm.addRow(obj);
        }
        
    }
    catch(SQLException ex){
        JOptionPane.showMessageDialog(null, ex);
    }
    }
